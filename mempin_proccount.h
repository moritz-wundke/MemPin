#ifndef MEMPIN_PROCCOUNT_H
#define MEMPIN_PROCCOUNT_H

//
// Tool entry points
//

BOOL proccount(INT32 toolId);

// Holds instruction count for a single procedure
typedef struct RtnCount
{
    string _name;
    string _image;
    ADDRINT _address;
    RTN _rtn;
    UINT64 _rtnCount;
    UINT64 _icount;
    struct RtnCount * _next;
} RTN_COUNT;

// Linked list of instruction counts for each routine
extern RTN_COUNT * RtnList;

/** Procedure counter */
VOID Proccount_docount(UINT64 * counter);

/** Instruction instrumentation */
VOID Proccount_Instruction(RTN rtn, VOID *v);

/** Finish callback */
VOID Proccount_Fini(INT32 code, VOID *v);

#endif // MEMPIN_PROCCOUNT_H